class BSTIterator(object):
    def __init__(self, root):
        """
        :type root: TreeNode
        """

    def next_recursive(self):
        """
        :rtype: int
        """

    def next_iterative(self):
        """
        :rtype: int
        """

    def has_next(self):
        """
        :rtype: bool
        """
        if self.next_recursive():
            return True
        return False
